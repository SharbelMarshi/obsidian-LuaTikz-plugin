import esbuild from "esbuild";
import process from "process";
import { builtinModules } from "node:module";
import fs from "node:fs";

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === 'production');

const tikzjaxBootstrapPatch = {
	name: 'tikzjax-bootstrap-texdir',
	setup(build) {
		build.onLoad({ filter: /node-tikzjax[\\/]dist[\\/]bootstrap\.js$/ }, async (args) => {
			let contents = await fs.promises.readFile(args.path, 'utf8');
			contents = contents.replace(
				"(0, path_1.join)(__dirname, '../tex')",
				"((typeof window !== 'undefined' && window.__LUATIKZ_TEX_DIR) || (typeof globalThis !== 'undefined' && globalThis.__LUATIKZ_TEX_DIR) || (0, path_1.join)(__dirname, '../tex'))",
			);
			return { contents, loader: 'js' };
		});
	},
};

esbuild.build({
	banner: {
		js: banner,
	},
	entryPoints: ['main.ts'],
	bundle: true,
	plugins: [tikzjaxBootstrapPatch],
	external: [
		'obsidian',
		'electron',
		'@codemirror/autocomplete',
		'@codemirror/closebrackets',
		'@codemirror/collab',
		'@codemirror/commands',
		'@codemirror/comment',
		'@codemirror/fold',
		'@codemirror/gutter',
		'@codemirror/highlight',
		'@codemirror/history',
		'@codemirror/language',
		'@codemirror/lint',
		'@codemirror/matchbrackets',
		'@codemirror/panel',
		'@codemirror/rangeset',
		'@codemirror/rectangular-selection',
		'@codemirror/search',
		'@codemirror/state',
		'@codemirror/stream-parser',
		'@codemirror/text',
		'@codemirror/tooltip',
		'@codemirror/view',
		...builtinModules],
	format: 'cjs',
	watch: !prod,
	target: 'es2020',
	logLevel: "info",
	sourcemap: prod ? false : 'inline',
	treeShaking: true,
	outfile: 'main.js',
	platform: 'node',
}).catch(() => process.exit(1));
