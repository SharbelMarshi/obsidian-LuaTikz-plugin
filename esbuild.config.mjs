import esbuild from "esbuild";
import process from "process";
import { builtinModules, createRequire } from "node:module";
import fs from "node:fs";

const require = createRequire(import.meta.url);

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === 'production');

const punycodePath = require.resolve("punycode/");

const punycodeAliasPlugin = {
	name: "punycode-alias",
	setup(build) {
		build.onResolve({ filter: /^punycode\/$/ }, () => ({
			path: punycodePath,
		}));
		build.onResolve({ filter: /^punycode$/ }, () => ({
			path: punycodePath,
		}));
	},
};

const tikzjaxBootstrapPatch = {
	name: 'tikzjax-bootstrap-texdir',
	setup(build) {
		build.onLoad({ filter: /node-tikzjax[\\/]dist[\\/]bootstrap\.js$/ }, async (args) => {
			let contents = await fs.promises.readFile(args.path, 'utf8');
			contents = contents.replace(
				"(0, path_1.join)(__dirname, '../tex')",
				"((typeof window !== 'undefined' && window.__LUATIKZ_TEX_DIR) || (0, path_1.join)(__dirname, '../tex'))",
			);
			return { contents, loader: 'js' };
		});
	},
};

const externalBuiltins = builtinModules.filter(name => name !== 'punycode');

const obsidianExternals = [
	'obsidian',
	'electron',
	'@codemirror/autocomplete',
	'@codemirror/closebrackets',
	'@codemirror/collab',
	'@codemirror/commands',
	'@codemirror/comment',
	'@codemirror/fold',
	'@codemirror/gutter',
	'@codemirror/highlight',
	'@codemirror/history',
	'@codemirror/language',
	'@codemirror/lint',
	'@codemirror/matchbrackets',
	'@codemirror/panel',
	'@codemirror/rangeset',
	'@codemirror/rectangular-selection',
	'@codemirror/search',
	'@codemirror/state',
	'@codemirror/stream-parser',
	'@codemirror/text',
	'@codemirror/tooltip',
	'@codemirror/view',
	...externalBuiltins,
];

const buildOptions = {
	banner: { js: banner },
	bundle: true,
	plugins: [punycodeAliasPlugin, tikzjaxBootstrapPatch],
	format: 'cjs',
	target: 'es2020',
	logLevel: "info",
	treeShaking: true,
	platform: 'node',
};

async function buildAll() {
	await esbuild.build({
		...buildOptions,
		entryPoints: ['main.ts'],
		external: [...obsidianExternals, 'node-tikzjax'],
		sourcemap: prod ? false : 'inline',
		outfile: 'main.js',
	});

	await esbuild.build({
		...buildOptions,
		entryPoints: ['node_modules/node-tikzjax/dist/index.js'],
		minify: prod,
		sourcemap: false,
		outfile: 'tikzjax.js',
	});
}

buildAll().catch(() => process.exit(1));
