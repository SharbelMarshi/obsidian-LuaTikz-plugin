#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const texDir = path.join(projectRoot, 'node_modules/node-tikzjax/tex');
const outDir = path.join(projectRoot, 'generated');
const outFile = path.join(outDir, 'tikzjaxTexAssets.ts');

const requiredFiles = ['core.dump.gz', 'tex.wasm.gz', 'tex_files.tar.gz'];

if (!fs.existsSync(texDir)) {
	console.error('node-tikzjax tex folder not found. Run npm install first.');
	process.exit(1);
}

const assets = {};
for (const fileName of requiredFiles) {
	const filePath = path.join(texDir, fileName);
	if (!fs.existsSync(filePath)) {
		console.error(`Missing TikZJax tex file: ${filePath}`);
		process.exit(1);
	}
	assets[fileName] = fs.readFileSync(filePath).toString('base64');
}

const assetHash = createHash('sha256')
	.update(Object.entries(assets).map(([name, data]) => `${name}:${data}`).join('\n'))
	.digest('hex');

const lines = [
	'// Generated by scripts/generate-tikzjax-tex-assets.mjs. Do not edit.',
	`export const TIKZJAX_TEX_ASSET_HASH = '${assetHash}';`,
	'export const TIKZJAX_TEX_ASSETS_BASE64: Record<string, string> = {',
];

for (const [fileName, data] of Object.entries(assets)) {
	lines.push(`\t'${fileName}': '${data}',`);
}

lines.push('};', '');

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, `${lines.join('\n')}\n`, 'utf8');
console.log(`Generated ${path.relative(projectRoot, outFile)} (${assetHash.slice(0, 12)}…).`);
