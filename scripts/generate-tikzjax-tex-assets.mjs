#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const sourceTexDir = path.join(projectRoot, 'node_modules/node-tikzjax/tex');
const runtimeDir = path.join(projectRoot, 'tikzjax-tex');
const outDir = path.join(projectRoot, 'generated');
const outFile = path.join(outDir, 'tikzjaxTexAssets.ts');

const requiredFiles = ['core.dump.gz', 'tex.wasm.gz', 'tex_files.tar.gz'];

if (!fs.existsSync(sourceTexDir)) {
	console.error('node-tikzjax tex folder not found. Run npm install first.');
	process.exit(1);
}

fs.mkdirSync(runtimeDir, { recursive: true });

const fileHashes = [];
for (const fileName of requiredFiles) {
	const sourcePath = path.join(sourceTexDir, fileName);
	if (!fs.existsSync(sourcePath)) {
		console.error(`Missing TikZJax tex file: ${sourcePath}`);
		process.exit(1);
	}

	const bytes = fs.readFileSync(sourcePath);
	fileHashes.push(`${fileName}:${createHash('sha256').update(bytes).digest('hex')}`);
	fs.copyFileSync(sourcePath, path.join(runtimeDir, fileName));
}

const assetHash = createHash('sha256')
	.update(fileHashes.join('\n'))
	.digest('hex');

fs.writeFileSync(path.join(runtimeDir, '.luatikz-tex-hash'), `${assetHash}\n`, 'utf8');

const lines = [
	'// Generated by scripts/generate-tikzjax-tex-assets.mjs. Do not edit.',
	`export const TIKZJAX_TEX_ASSET_HASH = '${assetHash}';`,
	'export const TIKZJAX_TEX_RUNTIME_DIR = \'tikzjax-tex\';',
	'export const TIKZJAX_TEX_RUNTIME_FILES = [',
	...requiredFiles.map(fileName => `\t'${fileName}',`),
	'] as const;',
	'export type TikzJaxTexRuntimeFile = typeof TIKZJAX_TEX_RUNTIME_FILES[number];',
	'',
];

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, `${lines.join('\n')}\n`, 'utf8');
console.log(`Generated ${path.relative(projectRoot, outFile)} (${assetHash.slice(0, 12)}…).`);
console.log(`Copied TikZJax runtime files to ${path.relative(projectRoot, runtimeDir)}/.`);
